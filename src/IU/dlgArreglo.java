    private void btnFinalizarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnFinalizarActionPerformed
        // TODO add your handling code here:
        //int resp = JOptionPane.showConfirmDialog(this, "¿Desea guardar información de valores?", "BOCA DE COBRANZA 1", JOptionPane.YES_NO_OPTION, JOptionPane.QUESTION_MESSAGE);

        if (cboConcepto.getSelectedIndex() == 0) {
            Mensajes.Sistema("Seleccione el Concepto del arreglo");
            cboConcepto.requestFocus();
            cboConcepto.setPopupVisible(true);
        } else if (Integer.parseInt(txtTotal.getText().replace(".", "").replace(",", "")) <= 0) {
            Mensajes.Sistema("No ha detallado la cantidad de los valores.");
            switch (cboConcepto.getSelectedIndex()) {
                case 1 -> {
                    txt50.requestFocus();
                    txt50.selectAll();
                }
                case 2 -> {
                    txt50.requestFocus();
                    txt50.selectAll();
                }
                case 3 -> {
                    txtTotal.requestFocus();
                    txtTotal.selectAll();
                }
                default -> {
                }
            }

        } else {
            if (txtEvento.getText().equals("N")) {
                int resp = Mensajes.confirmar("¿Seguro que desea guardar la información de valores?");
                if (resp == JOptionPane.YES_OPTION) {
                    ControlCaja.addArreglo();
                    this.dispose();
                }
            } else if (txtEvento.getText().equals("M")) {
                int resp = Mensajes.confirmar("¿Seguro que desea modificar la información de valores?");
                if (resp == JOptionPane.YES_OPTION) {
                    ControlCaja.actArreglo();
                    this.dispose();
                }
            }

        }
    }//GEN-LAST:event_btnFinalizarActionPerformed

    private void cboConceptoActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_cboConceptoActionPerformed
        // TODO add your handling code here:
        switch (cboConcepto.getSelectedIndex()) {
            case 0 -> {
                Habilitar(false);
                txtTotal.setEditable(false);
                txtTotal.setText("0");
                btnFinalizar.setEnabled(true);
            }
            case 1 -> {
                String cierre = generarCodigos.ObtenerCodigo2("SELECT concepto FROM arreglo_caja_2 WHERE idcaja=" + dlgRegistroValores.txtCaja.getText() + " AND idboca=" + Timbrado.getIdBoca() + " AND concepto='C' AND estado='S'");
                System.out.println("Cierre: " + cierre);
                if (cierre == null) {
                    Habilitar(true);
                    txtTotal.setEditable(false);
                    txt50.requestFocus();
                    Calculadora();
                    btnFinalizar.setEnabled(true);
                } else {
                    Mensajes.Alerta("Esta boca de cobranza ya registro el cierre de valores.");
                    Habilitar(false);
                    txtTotal.setEditable(false);
                    btnFinalizar.setEnabled(false);
                }

            }
            case 2 -> {
                String cierre = generarCodigos.ObtenerCodigo2("SELECT concepto FROM arreglo_caja_2 WHERE idcaja=" + dlgRegistroValores.txtCaja.getText() + " AND idboca=" + Timbrado.getIdBoca() + " AND concepto='C' AND estado='S'");
                System.out.println("Cierre: " + cierre);
                if (cierre == null) {
                    if (txtEvento.getText().equals("N")) {
                        Mensajes.Alerta("¡Observación importante!\r\nAl registrar el CIERRE DE VALORES ya no podra efectuar ventas en esta caja.\r\nFavor tener en cuenta esta información");
                        btnFinalizar.setEnabled(true);
                    }
                    Habilitar(true);
                    txtTotal.setEditable(false);
                    txt50.requestFocus();
                    Calculadora();
                } else {
                    Mensajes.Alerta("Esta boca de cobranza ya registro el cierre de valores.");
                    Habilitar(false);
                    txtTotal.setEditable(false);
                    btnFinalizar.setEnabled(false);
                }

            }
            case 3 -> {
                String cierre = generarCodigos.ObtenerCodigo2("SELECT concepto FROM arreglo_caja_2 WHERE idcaja=" + dlgRegistroValores.txtCaja.getText() + " AND idboca=" + Timbrado.getIdBoca() + " AND concepto='C' AND estado='S'");
                System.out.println("Cierre: " + cierre);
                if (cierre == null) {
                    Habilitar(false);
                    txtTotal.setEditable(true);
                    txtTotal.requestFocus();
                    txtTotal.setText("0");
                    btnFinalizar.setEnabled(true);
                } else {
                    Mensajes.Alerta("Esta boca de cobranza ya registro el cierre de valores.");
                    Habilitar(false);
                    txtTotal.setEditable(false);
                    btnFinalizar.setEnabled(false);
                }

            }
            default -> {
            }
        }

    }//GEN-LAST:event_cboConceptoActionPerformed


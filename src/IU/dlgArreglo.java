    public static final void Calculadora() {
        int cincuenta = Integer.parseInt(txt50.getText().replace(".", "").replace(",", "")) * 50;
        int cien = Integer.parseInt(txt100.getText().replace(".", "").replace(",", "")) * 100;
        int quinientos = Integer.parseInt(txt500.getText().replace(".", "").replace(",", "")) * 500;
        int mil = Integer.parseInt(txt1000.getText().replace(".", "").replace(",", "")) * 1000;
        int dos_mil = Integer.parseInt(txt2000.getText().replace(".", "").replace(",", "")) * 2000;
        int cinco_mil = Integer.parseInt(txt5000.getText().replace(".", "").replace(",", "")) * 5000;
        int diez_mil = Integer.parseInt(txt10000.getText().replace(".", "").replace(",", "")) * 10000;
        int veinte_mil = Integer.parseInt(txt20000.getText().replace(".", "").replace(",", "")) * 20000;
        int cincuenta_mil = Integer.parseInt(txt50000.getText().replace(".", "").replace(",", "")) * 50000;
        int cien_mil = Integer.parseInt(txt100000.getText().replace(".", "").replace(",", "")) * 100000;

        int total = (cincuenta + cien + quinientos + mil + dos_mil + cinco_mil + diez_mil + veinte_mil + cincuenta_mil + cien_mil);
        DecimalFormat df = new DecimalFormat("#,###");
        txtTotal.setText(df.format(total));
    }

